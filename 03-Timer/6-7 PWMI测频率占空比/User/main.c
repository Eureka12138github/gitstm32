/**
 ******************************************************************************
 * @file    main.c
 * @author  
 * @version V1.0
 * @date    2024年
 * @brief   PWMI模式测频率占空比演示程序
 ******************************************************************************
 * @details 
 * 功能概述：
 * 本程序使用TIM3的PWMI（PWM输入）模式，同时测量PA6引脚输入信号的频率和占空比，
 * 并通过OLED实时显示测量结果。测试信号由PA0输出的PWM信号提供。
 * 
 * 硬件连接：
 * - PA0：PWM信号输出（连接到PA6）
 * - PA6：频率和占空比测量输入
 * - OLED：显示测量结果
 * 
 * 技术原理：
 * 采用PWMI双通道捕获技术，通道1捕获上升沿测周期，通道2捕获下降沿测高电平时间，
 * 从而同时获得频率和占空比两个参数
 ******************************************************************************
 */

/*
 * 频率与占空比测量原理说明：
 * 
 * 1. 测量方法选择：测周法
 *    - 计数频率 fc = 1MHz（PSC=72-1，72MHz/72）
 *    - 计数器位数：16位（ARR=65536-1）
 *    - 分辨率 = 1MHz/65536 ≈ 15.26Hz
 *    - 测量范围：15Hz - 500kHz
 * 
 * 2. 中界频率指导选型：
 *    公式：fm = √(fc/T)
 *    其中：fc=1MHz，T=1s（典型测频时间窗口）
 *    得：fm = √(10⁶/1) = 1000Hz
 *    
 *    意义：在1000Hz以下测周法精度更高，以上测频法更优
 *    本项目测试信号1kHz，正好在中界频率附近，两种方法均可
 * 
 * 3. PWMI模式工作原理：
 *    - 通道1：自动捕获上升沿 → 测量完整周期
 *    - 通道2：自动捕获下降沿 → 测量高电平时间
 *    - 硬件自动完成双通道同步捕获
 *    - 占空比 = (高电平时间/周期时间) × 100%
 * 
 * 4. 关于CCR异常问题的分析：
 *    原始注释中的疑问："当CCR≥100时频率显示异常"
 *    
 *    根本原因：
 *    - PWM配置中ARR=100-1=99（常见配置）
 *    - CCR最大值应为99，当CCR≥100时超出范围
 *    - 硬件会自动截断或产生溢出，导致PWM波形失真
 *    - 实际输出频率偏离理论值，出现22Hz等异常值
 *    
 *    解决方案：
 *    - 确保CCR < ARR+1
 *    - 或修改ARR值扩大范围
 *    - 添加参数校验机制
 */

#include "stm32f10x.h"                 // STM32F10x系列设备头文件
#include "Delay.h"                     // 延时函数库
#include "LED.h"                       // LED驱动库
#include "OLED.h"                      // OLED显示驱动库
#include "PWM.h"                       // PWM信号发生器库
#include "IC.h"                        // 输入捕获测频占空比库

/**
 * @brief 主函数
 * @details 程序入口点
 */
int main(void)
{
    // 系统初始化
    IC_Init();        // 初始化输入捕获模块（TIM3 PWMI模式）
    OLED_Init();      // 初始化OLED显示屏
    PWM_Init();       // 初始化PWM信号发生器（TIM2）
    
    // 显示界面初始化
    OLED_ShowString(1, 1, "Freq:00000Hz");  // 频率显示行
    OLED_ShowString(2, 1, "Duty:00%");     // 占空比显示行
    
    // 配置测试信号参数
    PWM_SetPrescaler(720 - 1);  // 设置PWM频率 = 72MHz/(720×100) = 1kHz
    PWM_SetCompare1(68);        // 设置占空比 = 68/100 = 68%
    
    // 主循环 - 持续测量并显示
    while(1)
    {
        OLED_ShowNum(1, 6, IC_GetFreq(), 5);   // 显示5位频率值
        OLED_ShowNum(2, 6, IC_GetDuty(), 2);   // 显示2位占空比值
    }
}
