#include "stm32f10x.h"                  // 设备头文件
#include "PWM.h"

/**
 * @brief 舵机初始化函数
 * @details 初始化PWM模块用于舵机控制
 */
void Servo_Init(void)
{
    PWM_Init();  // 初始化PWM输出
}

/*
 * 舵机角度与脉冲宽度对应关系说明：
 * 
 * 舵机控制原理：
 * 标准舵机通过PWM脉冲宽度控制角度
 * - 0°   对应  500μs 脉冲宽度
 * - 180° 对应 2500μs 脉冲宽度
 * 
 * 线性关系推导：
 * 脉冲宽度 = 500 + (角度 × 2000/180)
 * 化简得：脉冲宽度 = 500 + (角度 × 100/9)
 * 
 * 在代码中的实现：
 * CCR值 = (100 × 角度/9) + 500
 * 
 * 验证几个关键点：
 * - 角度0°：  CCR = (100×0/9) + 500 = 500
 * - 角度90°： CCR = (100×90/9) + 500 = 1500  
 * - 角度180°：CCR = (100×180/9) + 500 = 2500
 */

/**
 * @brief 设置舵机角度
 * @param Angle 目标角度（0-180度）
 * @details 将角度转换为对应的PWM比较值控制舵机
 * @note 输入角度范围应在0-180度之间
 */
void Servo_SetAngle(float Angle)
{
    // 角度转PWM比较值的计算公式
    PWM_SetCompare2((100 * Angle / 9) + 500);
}
